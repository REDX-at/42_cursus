#include "philo.h"
#include <stdio.h>
#include <stdlib.h>

long	gettime(int time_code)
{
	struct timeval	tv;

	gettimeofday(&tv, NULL);
	if (MILLISECOND == time_code)
		return (tv.tv_sec * 1e3 + tv.tv_usec / 1e3);
	else if (MICROSECOND == time_code)
		return (tv.tv_sec * 1e6 + tv.tv_usec);
	else if (SECONDS == time_code)
		return (tv.tv_sec + tv.tv_usec / 1e6);
	else
		print_error("Wrong input to gettime:"
			"use <MILLISECOND> <MICROSECOND> <SECONDS>");
	return (1);
}

void	ft_usleep(long usec, t_table *table)
{
	long	start;
	long	elapsed;
	long	rem;

	start = gettime(MICROSECOND);
	while (gettime(MICROSECOND) - start < usec)
	{
		if (simulation_finished(table))
			break ;
		elapsed = gettime(MICROSECOND) - start;
		rem = usec - elapsed;
		if (rem > 1e4)
			usleep(rem / 2);
		else
			while (gettime(MICROSECOND) - start < usec)
				;
	}
}

void	clean(t_table *table)
{
	t_philo	*philo;
	int		i;

	i = -1;
	while (++i < table->philo_nbr)
	{
		philo = table->philos + i;
		pro_mutex(&philo->philo_mutex, DESTROY);
	}
	pro_mutex(&table->print_mutex, DESTROY);
	pro_mutex(&table->table_mutex, DESTROY);
	free(table->forks);
	free(table->philos);
}

int	print_error(const char *error)
{
	printf(RED"Error : "W"%s\n", error);
	return (0);
}
